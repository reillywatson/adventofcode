package main

import (
	"fmt"
	"strings"
)

func main() {
	var grid [][]rune
	for _, line := range strings.Split(input, "\n") {
		grid = append(grid, []rune(line))
	}
	numRemoved := 0
	for {
		accessible := 0
		for row := 0; row < len(grid); row++ {
			for col := 0; col < len(grid[row]); col++ {
				if grid[row][col] != '@' {
					continue
				}
				numAdjacent := 0
				if row > 0 && col > 0 && grid[row-1][col-1] == '@' {
					numAdjacent++
				}
				if row > 0 && grid[row-1][col] == '@' {
					numAdjacent++
				}
				if row > 0 && col < len(grid[row])-1 && grid[row-1][col+1] == '@' {
					numAdjacent++
				}
				if col > 0 && grid[row][col-1] == '@' {
					numAdjacent++
				}
				if col < len(grid[row])-1 && grid[row][col+1] == '@' {
					numAdjacent++
				}
				if row < len(grid)-1 && col > 0 && grid[row+1][col-1] == '@' {
					numAdjacent++
				}
				if row < len(grid)-1 && grid[row+1][col] == '@' {
					numAdjacent++
				}
				if row < len(grid)-1 && col < len(grid[row])-1 && grid[row+1][col+1] == '@' {
					numAdjacent++
				}
				if numAdjacent < 4 {
					accessible++
					numRemoved++
					grid[row][col] = '.'
				}
			}
		}
		if accessible == 0 {
			break
		}
	}
	fmt.Println(numRemoved)
}

