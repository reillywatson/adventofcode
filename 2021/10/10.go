package main

import (
	"fmt"
	"sort"
	"strings"
)

func main() {
	var scores []int
lineLoop:
	for _, line := range strings.Split(input, "\n") {
		score := 0
		stack := []rune{}
		scoreTable := map[rune]int{
			')': 1,
			']': 2,
			'}': 3,
			'>': 4,
		}
		for _, c := range line {
			switch c {
			case '(', '[', '<', '{':
				stack = append(stack, c)
			case ')', ']', '>', '}':
				if len(stack) == 0 {
					panic("bad kind of corruption")
				}
				if match[c] != stack[len(stack)-1] {
					score += scoreTable[c]
					continue lineLoop
				}
				stack = stack[:len(stack)-1]
			}
		}
		if len(stack) > 0 {
			for i := len(stack) - 1; i >= 0; i-- {
				score = score * 5
				score = score + scoreTable[match[stack[i]]]
			}
			scores = append(scores, score)
		}
	}
	sort.Ints(scores)
	fmt.Println(scores[len(scores)/2])
}

